import useCircleMarkers from "./useCircleMarkers";
//import Circle from "../../types/Circle/Circle";

describe("useCircleMarkers", () => {
  it("returns a string", () => {
    const result = useCircleMarkers({
      mapName: "mapName",
      circleMarkers: [
        {
          location: [1, 2],
          popup: "popup",
          tooltip: "tooltip",
          radius: 1,
        },
      ],
    });
    expect(typeof result).toBe("string");
  });

  it("respects map name", () => {
    const mapName = (Math.random() + 1).toString(36).substring(7);
    const result = useCircleMarkers({
      mapName,
      circleMarkers: [
        {
          location: [0, 0],
        },
      ],
    });
    expect(result).toContain(mapName);
    expect(1).toBe(1);
  });
});
